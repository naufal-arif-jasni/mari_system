        minute: '2-digit'
    });
    
    // Determine report title based on current filters
    const urlParams = new URLSearchParams(window.location.search);
    const searchTerm = urlParams.get('search') || '';
    const filterStatus = urlParams.get('filter_status') || '';
    
    let reportTitle = "MARI Application Report";
    if (searchTerm) {
        reportTitle += " - Search: " + searchTerm;
    }
    if (filterStatus) {
        reportTitle += " - Status: " + filterStatus;
    }
    
