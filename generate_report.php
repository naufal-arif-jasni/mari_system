    modal.classList.add('active');
}

/**
 * Get Status Badge Color
 */
function getStatusColor(status) {
    switch(status.toLowerCase()) {
        case 'pending':
            return '#fff3cd; color: #856404';
        case 'approved':
            return '#d4edda; color: #155724';
        case 'rejected':
            return '#f8d7da; color: #721c24';
        case 'under review':
            return '#cfe2ff; color: #084298';
        default:
            return '#e9ecef; color: #333';
    }
}

/**
 * Print Report
 * Opens print dialog for the report content
 */
function printReport() {
    const reportContent = document.getElementById('reportContent').innerHTML;
    const printWindow = window.open('', '_blank');
    
    printWindow.document.write(`
        <!DOCTYPE html>
        <html>
        <head>
            <title>MARI Application Report</title>
            <style>
                * { margin: 0; padding: 0; box-sizing: border-box; }
                body { font-family: Arial, sans-serif; padding: 20px; }
                @media print {
                    body { padding: 10px; }
                    .btn-print-report { display: none; }
                }
            </style>
        </head>
        <body>
            ${reportContent}
        </body>
        </html>
    `);
    
    printWindow.document.close();
    printWindow.focus();
    
    setTimeout(() => {
        printWindow.print();
        printWindow.close();
    }, 250);
}